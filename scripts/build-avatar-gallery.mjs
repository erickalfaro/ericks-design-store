const esc = (s) =>
  String(s).replace(/[&<>"']/g, (c) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[c]));

/* --------------------------------------------------------------- registry.json */

function buildRegistry() {
  const styleMeta = STYLES.map((s) => ({
    id: s.id, name: s.name, provider: s.provider, library: s.library,
    family: s.family, license: s.license, animated: s.animated, offline: s.offline,
    output: s.output, blurb: s.blurb, home: s.home,
  }));
  return {
    generatedBy: 'scripts/build-avatar-gallery.mjs',
    domain: 'avatars',
    note: 'Seed → deterministic avatar. Every offline style is STATIC; none animate natively (see docs/avatars/animation.md).',
    totalStyles: STYLES.length,
    families: FAMILIES,
    previewSeeds: PREVIEW_SEEDS,
    styles: styleMeta,
    referenceOnly: REFERENCE,
  };
}

/* ------------------------------------------------------------------ styles.md */

function buildStylesDoc(reg) {
  const byFam = (fam) => STYLES.filter((s) => s.family === fam);
  const licenseCell = (s) =>
    `[${s.license.name}](${s.license.url})` +
    (s.license.attribution ? ' · attribution' : '') + (s.license.restricted ? ' · ⚠ restricted' : '');
  let md = `# Avatar styles — generated reference

> Generated by \`scripts/build-avatar-gallery.mjs\` from \`scripts/avatars-core.mjs\`. Do not edit by hand.
> For the "when to use" judgment and the animation finding, see [index.md](index.md) and [animation.md](animation.md).

**${STYLES.length} offline styles**, all deterministic (\`<style>:<seed>\` → the same avatar every time) and all **static** — none animate natively. Grab any one:

\`\`\`bash
node scripts/avatar.mjs <style>:<seed> [--size N] [--format svg|datauri|png|html] [--out FILE]
\`\`\`
`;
  for (const fam of Object.keys(FAMILIES)) {
    const styles = byFam(fam);
    if (!styles.length) continue;
    md += `\n## ${FAMILIES[fam].label}\n\n_${FAMILIES[fam].blurb}_\n\n`;
    md += `| Style (\`id\`) | Provider | Output | License | Notes |\n|---|---|---|---|---|\n`;
    for (const s of styles) {
      md += `| \`${s.id}\` | [${s.provider}](${s.home}) | ${s.output} | ${licenseCell(s)} | ${s.blurb} |\n`;
    }
  }
  md += `\n## Reference-only (not wired into the offline CLI)\n\n`;
  md += `Real generators from the same landscape that are an HTTP API, another language, a browser-only runtime, or (the animation forks) out of scope. See [index.md](index.md).\n\n`;
  md += `| Option | Category | Why not offline-CLI | License | Reach it via |\n|---|---|---|---|---|\n`;
  for (const r of REFERENCE) {
    md += `| **${r.name}**${r.animated ? ' 🎬' : ''} | ${r.category} | \`${r.offline}\` | ${r.license} | ${r.reach} |\n`;
  }
  md += `\n_🎬 = the only options found with native animation (out-of-scope Avataaars forks)._\n`;
  md += `\n---\n_Regenerate: \`npm run avatars\`._\n`;
  return md;
}

