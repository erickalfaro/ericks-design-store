#!/usr/bin/env node
// Generate the avatar domain's outputs from the single source of truth (avatars-core.mjs):
//
//   data/avatars/registry.json   — pure metadata for every style + reference-only option
//   gallery/avatars.html         — live, seed-driven, fully-offline browser (esbuild bundle inlined)
//   docs/avatars/styles.md       — generated reference table (the prose docs are hand-written)
//
//   npm run avatars   (or: node scripts/build-avatar-gallery.mjs)
//
// The gallery inlines a browser bundle of avatars-core so it renders the SAME output the
// CLI produces, from any seed the user types — no network, no per-project dependency.

