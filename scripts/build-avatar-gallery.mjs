async function main() {
  await mkdir(GALLERY_DIR, { recursive: true });
  await mkdir(DOCS_DIR, { recursive: true });
  await mkdir(DATA_DIR, { recursive: true });

  const registry = buildRegistry();
  await writeFile(join(DATA_DIR, 'registry.json'), JSON.stringify(registry, null, 2) + '\n');
  console.log('  wrote data/avatars/registry.json (' + STYLES.length + ' styles, ' + REFERENCE.length + ' reference-only)');

  await writeFile(join(DOCS_DIR, 'styles.md'), buildStylesDoc(registry));
  console.log('  wrote docs/avatars/styles.md');

  console.log('  bundling browser generators with esbuild…');
  const built = await esbuild.build({
    entryPoints: [join(ROOT, 'scripts', 'avatars-browser.mjs')],
    bundle: true, format: 'iife', platform: 'browser', minify: true, write: false,
    define: { 'process.env.NODE_ENV': '"production"' }, logLevel: 'warning',
  });
  const bundleJs = built.outputFiles[0].text;
  await writeFile(join(GALLERY_DIR, 'avatars.html'), buildGalleryHtml(bundleJs));
  console.log('  wrote gallery/avatars.html (' + (bundleJs.length / 1024 / 1024).toFixed(2) + ' MB bundle inlined)');

  await buildDemo();

  console.log('\nAvatar gallery + registry + docs + demo built. Open gallery/avatars.html or gallery/avatars-animated.html.');
}

main().catch((err) => { console.error(err); process.exit(1); });
